# 숫자를 입력받아 100보다 큰수인지 100인지 작은수인지 출력
num = int(input("숫자를 입력하세요."))
if num > 100:
    print("100보다 큰 수입니다.")
elif num == 100:
    print("100입니다.")
else:
    print("100보다 작은 수입니다.")

